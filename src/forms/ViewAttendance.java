    private void loadDataInTable() {

        List<String> columns = new ArrayList<>(Arrays.asList(
                "ID", "Name", "Gender", "Email", "Date", "CheckIn", "CheckOut", "Status"
        ));

        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(columns.toArray());
        userTable.setModel(model);

        try {

            String searchText = txtSearch.getText().trim();

            Date fromDateCal = dateChooserFrom.getDate();
            Date toDateCal = dateChooserTo.getDate();

            LocalDate fromDate = null;
            LocalDate toDate = null;

            if (fromDateCal != null) {
                fromDate = fromDateCal.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            }
            if (toDateCal != null) {
                toDate = toDateCal.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            }

            Connection con = ConnectionProvider.getCon();
            java.sql.Statement st = con.createStatement();

            // 🔥 SEARCH VALIDATION
            Integer searchId = null;
            if (!searchText.isEmpty()) {
                try {
                    searchId = Integer.parseInt(searchText);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Enter valid numeric ID");
                    return;
                }
            }

            // 🔥 SPECIAL CASE: ID + DATE RANGE
            if (searchId != null && fromDate != null && toDate != null) {

                String sqlQuery = "SELECT ud.id, ud.name, ud.gender, ud.email, "
                        + "ua.date, ua.checkin, ua.checkout "
                        + "FROM userdetails ud "
                        + "LEFT JOIN userattendance ua ON ud.id = ua.userid "
                        + "WHERE ud.id = " + searchId;

                ResultSet rs = st.executeQuery(sqlQuery);

                Map<LocalDate, String[]> attendanceMap = new HashMap<>();
                String name = "", gender = "", email = "";

                boolean found = false;

                while (rs.next()) {
                    found = true;

                    name = rs.getString("name");
                    gender = rs.getString("gender");
                    email = rs.getString("email");

                    if (rs.getString("date") != null) {
                        LocalDate date = LocalDate.parse(rs.getString("date"));
                        String checkIn = rs.getString("checkin");
                        String checkOut = rs.getString("checkout");

                        attendanceMap.put(date, new String[]{checkIn, checkOut});
                    }
                }

                if (!found) {
                    JOptionPane.showMessageDialog(null, "Student Not Found");
                    return;
                }

                int presentCount = 0;
                int absentCount = 0;

                LocalDate currentDate = fromDate;

                while (!currentDate.isAfter(toDate)) {

                    String[] record = attendanceMap.get(currentDate);

                    String checkIn = null;
                    String checkOut = null;

                    if (record != null) {
                        checkIn = record[0];
                        checkOut = record[1];
                    }

                    String status;

                    if (checkIn != null && checkOut != null) {
                        status = "Present";
                        presentCount++;
                    } else {
                        status = "Absent";
                        absentCount++;
                    }

                    model.addRow(new Object[]{
                        searchId, name, gender, email,
                        currentDate.toString(),
                        checkIn == null ? "-" : checkIn,
                        checkOut == null ? "-" : checkOut,
                        status
                    });

                    currentDate = currentDate.plusDays(1);
                }

                lblPresent.setText(String.valueOf(presentCount));
                lblAbsent.setText(String.valueOf(absentCount));

                return;
            }

            // 🔥 NORMAL CASE
            String sqlQuery;

            if (fromDate != null && toDate != null) {
                sqlQuery = "SELECT ud.id, ud.name, ud.gender, ud.email, "
                        + "ua.date, ua.checkin, ua.checkout "
                        + "FROM userdetails ud "
                        + "LEFT JOIN userattendance ua ON ud.id = ua.userid "
                        + "AND ua.date BETWEEN '" + fromDate + "' AND '" + toDate + "' ";
            } else if (fromDate != null) {
                sqlQuery = "SELECT ud.id, ud.name, ud.gender, ud.email, "
                        + "ua.date, ua.checkin, ua.checkout "
                        + "FROM userdetails ud "
                        + "LEFT JOIN userattendance ua ON ud.id = ua.userid "
                        + "AND ua.date = '" + fromDate + "' ";
            } else {
                sqlQuery = "SELECT ud.id, ud.name, ud.gender, ud.email, "
                        + "ua.date, ua.checkin, ua.checkout "
                        + "FROM userdetails ud "
                        + "LEFT JOIN userattendance ua ON ud.id = ua.userid ";
            }

            if (searchId != null) {
                sqlQuery += "WHERE ud.id = " + searchId;
            }

            ResultSet rs = st.executeQuery(sqlQuery);

            int presentCount = 0;
            int absentCount = 0;
            boolean found = false;

            while (rs.next()) {
                found = true;

                String checkIn = rs.getString("checkin");
                String checkOut = rs.getString("checkout");

                String status;

                if (checkIn != null && checkOut != null) {
                    status = "Present";
                    presentCount++;
                } else {
                    status = "Absent";
                    absentCount++;
                }

                model.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("name"),
                    rs.getString("gender"),
                    rs.getString("email"),
                    rs.getString("date") == null ? "-" : rs.getString("date"),
                    checkIn == null ? "-" : checkIn,
                    checkOut == null ? "-" : checkOut,
                    status
                });
            }

            if (!found && searchId != null) {
                JOptionPane.showMessageDialog(null, "Student Not Found");
            }

            lblPresent.setText(String.valueOf(presentCount));
            lblAbsent.setText(String.valueOf(absentCount));

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }

    private Long countWeekdays(LocalDate start, LocalDate end) {
        long count = 0;
        LocalDate date = start;
        while (date.isBefore(end) || date.equals(end)) {
            if (!(date.getDayOfWeek() == DayOfWeek.SATURDAY || date.getDayOfWeek() == DayOfWeek.SUNDAY)) {
                count++;
            }
            date = date.plusDays(1);
        }
        return count;
    }
