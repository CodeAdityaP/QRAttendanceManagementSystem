    private void btnGenerateReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateReportActionPerformed
        try {

            loadDataInTable();

            Document document = new Document();
            PdfWriter.getInstance(document, new FileOutputStream("C:\\Users\\Aditya Pandit\\Downloads\\AttendanceReport.pdf"));
            document.open();

            document.add(new Paragraph("STUDENT ATTENDANCE REPORT\n\n"));

            String fromDate = (dateChooserFrom.getDate() != null) ? dateChooserFrom.getDate().toString() : "N/A";
            String toDate = (dateChooserTo.getDate() != null) ? dateChooserTo.getDate().toString() : "N/A";

            document.add(new Paragraph("From Date: " + fromDate));
            document.add(new Paragraph("To Date: " + toDate + "\n\n"));

            PdfPTable pdfTable = new PdfPTable(userTable.getColumnCount());

            // Column headers
            for (int i = 0; i < userTable.getColumnCount(); i++) {
                pdfTable.addCell(userTable.getColumnName(i));
            }

            for (int rows = 0; rows < userTable.getRowCount(); rows++) {
                for (int cols = 0; cols < userTable.getColumnCount(); cols++) {
                    Object value = userTable.getValueAt(rows, cols);
                    pdfTable.addCell(value == null ? "" : value.toString());
                }
            }

            document.add(pdfTable);

            if (lblPresent.isVisible() && lblAbsent.isVisible()) {
                document.add(new Paragraph("\n"));
                document.add(new Paragraph("Total Present: " + lblPresent.getText()));
                document.add(new Paragraph("Total Absent: " + lblAbsent.getText()));
            }

            document.close();

            JOptionPane.showMessageDialog(null, "Report Generated Successfully!");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }

    }//GEN-LAST:event_btnGenerateReportActionPerformed

